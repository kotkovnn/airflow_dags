CREATE TABLE IF NOT EXISTS cannabis (
    id int
    ,uid uuid
    ,strain varchar(200)
    ,cannabinoid_abbreviation varchar(200)
    ,cannabinoid varchar(200)
    ,terpene varchar(200)
    ,medical_use varchar(200)
    ,health_benefit varchar(200)
    ,category varchar(200)
    ,type varchar(200)
    ,buzzword varchar(200)
    ,brand varchar(200)
);
